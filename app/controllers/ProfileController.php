class ProfileController extends \BaseController {


	public function getProfile($username){
		
		$user = User::where('username', '=', $username);
		
		if($user->count()) {
			$user = $user->first();
			
			return View::make('profile.user')->with('user', $user);
		}
		
		return 'User not found';
	}

